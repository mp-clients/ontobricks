/**
 * OntoBricks - mapping-import.js
 * Import section for the Mapping page - R2RML file import (local + Unity Catalog)
 */

document.addEventListener('DOMContentLoaded', function() {
    // --- R2RML Import: Local file ---
    document.getElementById('importR2rmlLocalBtn')?.addEventListener('click', function() {
        document.getElementById('importR2rmlFileInput')?.click();
    });

    // --- R2RML Import: file input change handler ---
    document.getElementById('importR2rmlFileInput')?.addEventListener('change', async function(e) {
        const file = e.target.files[0];
        if (!file) return;

        try {
            showNotification('Importing R2RML file...', 'info', 2000);
            const content = await file.text();
            await importR2rmlContent(content, file.name);
        } catch (error) {
            showNotification('Error reading file: ' + error.message, 'error');
        }
        this.value = '';
    });
});

/**
 * Send R2RML content to the backend for parsing, update MappingState, and refresh the UI.
 */
async function importR2rmlContent(content, filename) {
    try {
        const response = await fetch('/mapping/parse-r2rml', {
            method: 'POST',
            headers: {'Content-Type': 'application/json'},
            body: JSON.stringify({ content: content }),
            credentials: 'same-origin'
        });

        const result = await response.json();

        if (result.success) {
            const entityCount = result.entities?.length || 0;
            const relCount = result.relationships?.length || 0;

            if (typeof MappingState !== 'undefined') {
                if (result.entities) {
                    MappingState.config.entities = result.entities;
                }
                if (result.relationships) {
                    MappingState.config.relationships = result.relationships;
                }
                if (entityCount > 0) {
                    MappingState.mappingValidated = true;
                    MappingState.r2rmlAutoGenerated = true;
                }
            }

            // Show inline result summary
            const resultDiv = document.getElementById('r2rmlImportResult');
            const resultText = document.getElementById('r2rmlImportResultText');
            if (resultDiv && resultText) {
                resultText.textContent =
                    `Imported from ${filename}: ${entityCount} entity mapping(s), ${relCount} relationship mapping(s).`;
                resultDiv.classList.remove('d-none');
            }

            if (typeof updateMappingCompletionStatus === 'function') {
                updateMappingCompletionStatus();
            }
            if (typeof window.refreshOntologyStatus === 'function') {
                window.refreshOntologyStatus();
            }

            showNotification(
                `R2RML imported: ${entityCount} entities, ${relCount} relationships from ${filename}`,
                'success'
            );
        } else {
            showNotification('Error parsing R2RML: ' + result.message, 'error');
        }
    } catch (error) {
        showNotification('Error importing R2RML: ' + error.message, 'error');
    }
}
